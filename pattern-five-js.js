document.getElementById("first-finger-c-five-submit").addEventListener("click", function(){
   let pitch = document.getElementById("first-finger-c-five").value

   if (pitch === "Db" || pitch === "db" || pitch === "D b" || pitch === "d b"){
      document.getElementById("first-finger-c-five").style.backgroundColor="#00FF00"}
      else{
         document.getElementById("first-finger-c-five").style.backgroundColor="#FF0000"}
      })

